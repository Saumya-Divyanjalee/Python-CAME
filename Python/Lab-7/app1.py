# Statement
# A statement is an instruction that Python can execute

# Expression statements
# An expression is a piece of code that produces a value
# In Python, an expression by itself is also treated as a statement

10 + 2 + 3      # expression statement (evaluated, but result is ignored)
'Ijse'          # expression statement (string literal, result ignored)
5 > 2           # expression statement (evaluates to True, but not shown)

# In scripts, expression results are NOT automatically displayed
# But in REPL (interactive mode), Python shows the result of expressions

print(10 + 5 + 2)  # statement with output
